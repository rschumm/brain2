public class Installiere {

    // die Sachen, die vom gui kommen:



    String server;

    String neueVersion;

    String artefakt;

    String stage;

    public String getServer() {
        return server;
    }

    public void setServer(String server) {
        this.server = server;
    }

    public String getNeueVersion() {
        return neueVersion;
    }

    public void setNeueVersion(String neueversion) {
        this.neueVersion = neueversion;
    }

    public String getArtefakt() {
        return artefakt;
    }

    public void setArtefakt(String artefakt) {
        this.artefakt = artefakt;
    }

    public String getStage() {
        return stage;
    }

    public void setStage(String stage) {
        this.stage = stage;
    }

}
